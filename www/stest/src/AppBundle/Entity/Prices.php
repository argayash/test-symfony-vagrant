<?php

namespace AppBundle\Entity;

/**
 * Prices
 */
class Prices
{
    /**
     * @var string
     */
    private $price;

    /**
     * @var integer
     */
    private $priceTypeId;

    /**
     * @var integer
     */
    private $goodsId;


    /**
     * Set price
     *
     * @param string $price
     *
     * @return Prices
     */
    public function setPrice($price)
    {
        $this->price = $price;

        return $this;
    }

    /**
     * Get price
     *
     * @return string
     */
    public function getPrice()
    {
        return $this->price;
    }

    /**
     * Set priceTypeId
     *
     * @param integer $priceTypeId
     *
     * @return Prices
     */
    public function setPriceTypeId($priceTypeId)
    {
        $this->priceTypeId = $priceTypeId;

        return $this;
    }

    /**
     * Get priceTypeId
     *
     * @return integer
     */
    public function getPriceTypeId()
    {
        return $this->priceTypeId;
    }

    /**
     * Set goodsId
     *
     * @param integer $goodsId
     *
     * @return Prices
     */
    public function setGoodsId($goodsId)
    {
        $this->goodsId = $goodsId;

        return $this;
    }

    /**
     * Get goodsId
     *
     * @return integer
     */
    public function getGoodsId()
    {
        return $this->goodsId;
    }
    /**
     * @var \AppBundle\Entity\Goods
     */
    private $goods;


    /**
     * Set goods
     *
     * @param \AppBundle\Entity\Goods $goods
     *
     * @return Prices
     */
    public function setGoods(\AppBundle\Entity\Goods $goods = null)
    {
        $this->goods = $goods;

        return $this;
    }

    /**
     * Get goods
     *
     * @return \AppBundle\Entity\Goods
     */
    public function getGoods()
    {
        return $this->goods;
    }
}
